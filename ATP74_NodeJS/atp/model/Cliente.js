class Cliente {
    nome;
    sobrenome;
    cc;

    get nome(){
        this.nome = nome;
    }

    set nome(onome){
        this.nome = onome;
    }

    get sobrenome(){
        this.sobrenome = sobrenome;
    }

    set sobrenome(osobrenome){
        this.sobrenome = osobrenome;
    }

    get cc(){
        this.cc = cc;
    }

    set cc(occ){
        this.cc = occ;
    }
}
export default Cliente;