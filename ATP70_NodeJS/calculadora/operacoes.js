

function soma (n1, n2){
resultado = n1 + n2
return resultado;
}

function subtracao (n1, n2){
    resultado = n1 - n2
    return resultado;
}

function multiplicacao (n1, n2){
    resultado = n1 * n2
    return resultado;
}

function divisao (n1, n2){
    resultado = n1 / n2
    return resultado;
}

console.log(soma(2,3));
console.log(subtracao (3, 4));
console.log(multiplicacao (5,6));
console.log(divisao(18, 8));

